  <script>
    AOS.init({
      once: true,
      duration: 1000,
      easing: 'ease-in-out'
    });
    document.body.classList.add('fade-in');

    document.querySelectorAll('a[href]').forEach(link => {
      const isExternal = link.target === '_blank' || link.href.startsWith('mailto:') || link.href.startsWith('tel:');
      if (!isExternal) {
        link.addEventListener('click', function (e) {
          const href = this.getAttribute('href');
          if (!href || href.startsWith('#')) return;
          e.preventDefault();
          document.body.classList.add('fade-out');
          setTimeout(() => { window.location.href = href; }, 400);
        });
      }
    });

    function openLogin() {
      document.getElementById('loginModal').style.display = 'flex';
    }
    function closeLogin() {
      document.getElementById('loginModal').style.display = 'none';
    }
    function openLogoutPopup() {
      document.getElementById('logoutModal').style.display = 'flex';
    }
    function closeLogoutPopup() {
      document.getElementById('logoutModal').style.display = 'none';
    }
    function logout() {
      window.location.href = 'logout.php';
    }
    window.onclick = function(e) {
      if (e.target == document.getElementById('loginModal')) closeLogin();
      if (e.target == document.getElementById('logoutModal')) closeLogoutPopup();
    }
  </script>
